//file: initarray.c
#include <stdio.h>
#define SIZE 6 

int main(void)
{
	//배열 score의 선언과 초기화 
	double score[] = { 89.3, 79.2, 84.83, 76.8, 92.52, 97.4 };
	double sum = 0;

	//for 문을 이용하여 합을 구함 
	for (int i = 0; i < SIZE; i++)
	{
		sum += score[i];
		printf("score[%d] = %.2f\n", i, score[i]);
	}
	printf("성적의 합은 %.2f이고 평균은 %.2f이다.\n", sum, sum / SIZE);

	return 0;
}
/*
score[0] = 89.30
score[1] = 79.20
score[2] = 84.83
score[3] = 76.80
score[4] = 92.52
score[5] = 97.40
성적의 합은 520.05이고 평균은 86.67이다.
*/